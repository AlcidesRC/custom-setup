# Set light theme
gsettings set org.gnome.shell.ubuntu color-scheme prefer-light
gsettings set org.gnome.desktop.interface color-scheme prefer-light

# Set theme
gsettings set org.gnome.desktop.interface gtk-theme Yaru-sage

# Set wallpaper
gsettings set org.gnome.desktop.background picture-uri-dark file:///home/$USER/.local/share/custom-setup/defaults/wallpapers/light.jpg
